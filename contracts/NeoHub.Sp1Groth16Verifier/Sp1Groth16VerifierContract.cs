/// <summary>
/// Verifies the v6.1-compatible Groth16 wrapper proofs emitted by the SP1 6.2.1 SDK
/// over BN254 for Neo N4 batch execution.
/// </summary>
/// <remarks>
/// See <c>doc.md</c> §8. The contract pins the SP1 wrapper selector, recursion VK root,
/// Groth16 verifying key, five-field public-input layout, and successful exit code. A new
/// SP1 circuit version requires a newly reviewed verifier deployment and router update.
/// </remarks>
[DisplayName("NeoHub.Sp1Groth16Verifier")]
[ContractAuthor("R3E Network", "dev@r3e.network")]
[ContractDescription("SP1 6.2.1 SDK v6.1-compatible Groth16/BN254 verifier for Neo N4 batch proofs.")]
[ContractVersion("0.1.0")]
[ContractSourceCode("https://github.com/r3e-network/neo-n4/tree/master/contracts/NeoHub.Sp1Groth16Verifier")]
[ContractPermission(
    "0x726cb6e0cd8628a1350a611384688911ab75f51b",
    "sha256",
    "bn254Deserialize",
    "bn254Add",
    "bn254Mul",
    "bn254Pairing",
    "bn254Equal")]
public class Sp1Groth16VerifierContract : SmartContract
{
    private const byte ProofSystemSp1 = 1;
    private const int ProgramVKeySize = 32;
    private const int PublicInputHashSize = 32;
    private const int SelectorSize = 4;
    private const int FieldElementSize = 32;
    private const int ProofHeaderSize = SelectorSize + 3 * FieldElementSize;
    private const int RawGroth16ProofSize = 256;
    private const int Sp1ProofSize = ProofHeaderSize + RawGroth16ProofSize;
    private const int G1Size = 64;
    private const int G2Size = 128;

    private const int ExitCodeOffset = SelectorSize;
    private const int VkRootOffset = ExitCodeOffset + FieldElementSize;
    private const int NonceOffset = VkRootOffset + FieldElementSize;
    private const int ProofAOffset = ProofHeaderSize;
    private const int ProofBOffset = ProofAOffset + G1Size;
    private const int ProofCOffset = ProofBOffset + G2Size;

    /// <summary>SP1 wrapper verifier selector derived from SHA-256 of the pinned Groth16 VK.</summary>
    [Safe]
    public static byte[] GetVerifierSelector() => ExpectedSelector();

    /// <summary>SP1 recursion verifying-key Merkle root pinned by this verifier version.</summary>
    [Safe]
    public static byte[] GetRecursionVkRoot() => ExpectedVkRoot();

    /// <summary>
    /// Verifies one SP1 Groth16 proof against the canonical N4 public-input hash.
    /// </summary>
    /// <param name="proofSystem">Must be <c>ProofSystem.Sp1</c> (1).</param>
    /// <param name="verificationKeyId">Raw big-endian SP1 program verification-key digest.</param>
    /// <param name="publicInputHash">Canonical N4 public-input hash committed by the guest.</param>
    /// <param name="proofBytes">SP1 on-chain proof bytes: selector, exit code, VK root, nonce, A, B, C.</param>
    /// <returns><see langword="true"/> only when the complete Groth16 pairing equation holds.</returns>
    [Safe]
    public static bool VerifyZkProof(
        byte proofSystem,
        byte[] verificationKeyId,
        byte[] publicInputHash,
        byte[] proofBytes)
    {
        verificationKeyId ??= new byte[0];
        publicInputHash ??= new byte[0];
        proofBytes ??= new byte[0];

        ExecutionEngine.Assert(proofSystem == ProofSystemSp1, "proofSystem must be SP1");
        ExecutionEngine.Assert(verificationKeyId.Length == ProgramVKeySize,
            "SP1 program vkey must be 32 bytes");
        ExecutionEngine.Assert(publicInputHash.Length == PublicInputHashSize,
            "publicInputHash must be 32 bytes");
        ExecutionEngine.Assert(proofBytes.Length == Sp1ProofSize,
            "SP1 Groth16 proof must be 356 bytes");
        ExecutionEngine.Assert(BytesEqual(proofBytes, 0, ExpectedSelector()),
            "SP1 verifier selector mismatch");
        ExecutionEngine.Assert(IsZero(proofBytes, ExitCodeOffset, FieldElementSize),
            "SP1 guest exit code must be zero");
        ExecutionEngine.Assert(BytesEqual(proofBytes, VkRootOffset, ExpectedVkRoot()),
            "SP1 recursion VK root mismatch");

        var publicValuesDigest = HashCommittedPublicValues(publicInputHash);
        var exitCode = ReadFixedBytes(proofBytes, ExitCodeOffset, FieldElementSize);
        var vkRoot = ReadFixedBytes(proofBytes, VkRootOffset, FieldElementSize);
        var nonce = ReadFixedBytes(proofBytes, NonceOffset, FieldElementSize);

        ExecutionEngine.Assert(IsCanonicalScalar(verificationKeyId), "SP1 program vkey is not in Fr");
        ExecutionEngine.Assert(IsCanonicalScalar(publicValuesDigest), "public-values digest is not in Fr");
        ExecutionEngine.Assert(IsCanonicalScalar(exitCode), "exit code is not in Fr");
        ExecutionEngine.Assert(IsCanonicalScalar(vkRoot), "recursion VK root is not in Fr");
        ExecutionEngine.Assert(IsCanonicalScalar(nonce), "proof nonce is not in Fr");

        var linearCombination = CryptoLib.Bn254Deserialize(Ic0());
        linearCombination = AddScaled(linearCombination, Ic1(), verificationKeyId);
        linearCombination = AddScaled(linearCombination, Ic2(), publicValuesDigest);
        linearCombination = AddScaled(linearCombination, Ic3(), exitCode);
        linearCombination = AddScaled(linearCombination, Ic4(), vkRoot);
        linearCombination = AddScaled(linearCombination, Ic5(), nonce);

        var proofA = CryptoLib.Bn254Deserialize(ReadFixedBytes(proofBytes, ProofAOffset, G1Size));
        var proofB = CryptoLib.Bn254Deserialize(ReadFixedBytes(proofBytes, ProofBOffset, G2Size));
        var proofC = CryptoLib.Bn254Deserialize(ReadFixedBytes(proofBytes, ProofCOffset, G1Size));

        var left = CryptoLib.Bn254Pairing(proofA, proofB);
        var alphaBeta = CryptoLib.Bn254Pairing(CryptoLib.Bn254Deserialize(Alpha()), CryptoLib.Bn254Deserialize(Beta()));
        var publicGamma = CryptoLib.Bn254Pairing(linearCombination, CryptoLib.Bn254Deserialize(Gamma()));
        var cDelta = CryptoLib.Bn254Pairing(proofC, CryptoLib.Bn254Deserialize(Delta()));
        var right = CryptoLib.Bn254Add(CryptoLib.Bn254Add(alphaBeta, publicGamma), cDelta);

        return CryptoLib.Bn254Equal(left, right);
    }

    private static object AddScaled(object accumulator, byte[] pointBytes, byte[] scalar)
    {
        var point = CryptoLib.Bn254Deserialize(pointBytes);
        return CryptoLib.Bn254Add(accumulator, CryptoLib.Bn254Mul(point, scalar));
    }

    private static byte[] HashCommittedPublicValues(byte[] publicInputHash)
    {
        var committed = new byte[1 + PublicInputHashSize];
        committed[0] = 0;
        for (var i = 0; i < PublicInputHashSize; i++) committed[i + 1] = publicInputHash[i];
        var digest = (byte[])CryptoLib.Sha256((ByteString)committed);
        digest[0] = (byte)(digest[0] & 0x1F);
        return digest;
    }

    private static bool IsCanonicalScalar(byte[] scalar)
    {
        if (scalar == null || scalar.Length != FieldElementSize) return false;
        var modulus = ScalarFieldModulus();
        for (var i = 0; i < FieldElementSize; i++)
        {
            if (scalar[i] < modulus[i]) return true;
            if (scalar[i] > modulus[i]) return false;
        }
        return false;
    }

    private static bool IsZero(byte[] value, int offset, int count)
    {
        for (var i = 0; i < count; i++) if (value[offset + i] != 0) return false;
        return true;
    }

    private static bool BytesEqual(byte[] value, int offset, byte[] expected)
    {
        if (offset < 0 || offset + expected.Length > value.Length) return false;
        for (var i = 0; i < expected.Length; i++)
            if (value[offset + i] != expected[i]) return false;
        return true;
    }

    private static byte[] ReadFixedBytes(byte[] value, int offset, int count)
    {
        var result = new byte[count];
        for (var i = 0; i < count; i++) result[i] = value[offset + i];
        return result;
    }

